"""Dense and restartable field-line trajectory tracing.

The public entry points in this module are object-first: pass a
``VectorFieldCylind``-compatible field object, not six separate cyna arrays.
Raw-array tracing remains available in :mod:`pyna.toroidal.flt.numba_poincare`
for low-level callers.
"""
from __future__ import annotations

from dataclasses import dataclass, field
import json
import math
import os
from pathlib import Path
import tempfile
from typing import Any

import numpy as np

from pyna.toroidal.flt.numba_poincare import field_arrays_from_field, trace_orbit_along_phi


_CHECKPOINT_NAME = "fieldline_trajectory_checkpoint.json"
_SCHEMA_VERSION = 1


@dataclass
class DenseFieldLineTrajectory:
    """Sampled field-line trajectory with scipy-like dense interpolation.

    ``sol(phi)`` returns an array with shape ``(2, n)`` containing ``R`` and
    ``Z`` at requested toroidal angles.  Interpolation is linear between stored
    cyna samples; the samples themselves are generated by cyna RK4 tracing.
    """

    phi: np.ndarray
    R: np.ndarray
    Z: np.ndarray
    alive: np.ndarray | None = None
    metadata: dict[str, Any] = field(default_factory=dict)
    storage_dir: str | None = None

    @property
    def n_samples(self) -> int:
        return int(len(self.phi))

    @property
    def status(self) -> str:
        return str(self.metadata.get("status", "complete"))

    def sol(self, phi_values):
        """Interpolate ``[R, Z]`` at scalar or array-like toroidal angles."""

        scalar = np.isscalar(phi_values)
        q = np.atleast_1d(np.asarray(phi_values, dtype=float))
        mask = np.isfinite(self.phi) & np.isfinite(self.R) & np.isfinite(self.Z)
        if self.alive is not None:
            mask &= np.asarray(self.alive, dtype=bool)
        if np.count_nonzero(mask) < 2:
            raise ValueError("trajectory has fewer than two valid samples")

        phi = np.asarray(self.phi[mask], dtype=float)
        R = np.asarray(self.R[mask], dtype=float)
        Z = np.asarray(self.Z[mask], dtype=float)
        if phi[0] > phi[-1]:
            phi = phi[::-1]
            R = R[::-1]
            Z = Z[::-1]
        out = np.vstack([np.interp(q, phi, R), np.interp(q, phi, Z)])
        return out[:, 0] if scalar else out

    __call__ = sol

    def as_arrays(self, *, copy: bool = False) -> dict[str, np.ndarray]:
        """Return trajectory arrays as ``{"phi", "R", "Z", "alive"}``."""

        arrays = {
            "phi": np.asarray(self.phi),
            "R": np.asarray(self.R),
            "Z": np.asarray(self.Z),
        }
        if self.alive is not None:
            arrays["alive"] = np.asarray(self.alive)
        if copy:
            arrays = {k: v.copy() for k, v in arrays.items()}
        return arrays

    def flush(self) -> None:
        """Flush memmap-backed arrays to disk."""

        for arr in (self.phi, self.R, self.Z, self.alive):
            if hasattr(arr, "flush"):
                arr.flush()


def _atomic_write_json(path: Path, payload: dict[str, Any]) -> None:
    tmp = path.with_suffix(path.suffix + ".tmp")
    with tmp.open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, sort_keys=True)
    os.replace(tmp, path)


def _load_checkpoint(path: Path) -> dict[str, Any] | None:
    if not path.exists():
        return None
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def _expected_n_samples(phi_start: float, phi_end: float, dphi_out: float) -> int:
    span = abs(float(phi_end) - float(phi_start))
    if span == 0.0:
        return 1
    return int(math.ceil(span / abs(float(dphi_out)))) + 1


def _allocate_array(path: Path | None, name: str, shape: tuple[int, ...], dtype, *, fill_value):
    if path is None:
        arr = np.empty(shape, dtype=dtype)
    else:
        arr = np.lib.format.open_memmap(path / f"{name}.npy", mode="w+", dtype=dtype, shape=shape)
    arr[...] = fill_value
    return arr


def _open_array(path: Path, name: str, mode: str = "r+"):
    return np.load(path / f"{name}.npy", mmap_mode=mode)


def _checkpoint_payload(
    *,
    checkpoint_dir: Path,
    status: str,
    params: dict[str, Any],
    n_total: int,
    n_written: int,
    next_index: int,
    last_R: float,
    last_Z: float,
    last_phi: float,
    storage: str,
) -> dict[str, Any]:
    return {
        "schema_version": _SCHEMA_VERSION,
        "status": status,
        "params": params,
        "n_total": int(n_total),
        "n_written": int(n_written),
        "next_index": int(next_index),
        "last_R": float(last_R),
        "last_Z": float(last_Z),
        "last_phi": float(last_phi),
        "storage": storage,
        "array_files": {
            "phi": str(checkpoint_dir / "phi.npy"),
            "R": str(checkpoint_dir / "R.npy"),
            "Z": str(checkpoint_dir / "Z.npy"),
            "alive": str(checkpoint_dir / "alive.npy"),
        },
    }


def _params_match(saved: dict[str, Any], current: dict[str, Any]) -> bool:
    keys = ("R0", "Z0", "phi_start", "phi_end", "DPhi", "dphi_out", "chunk_intervals")
    for key in keys:
        if key not in saved or key not in current:
            return False
        if isinstance(current[key], float):
            if not np.isclose(float(saved[key]), float(current[key]), rtol=0.0, atol=1.0e-14):
                return False
        elif saved[key] != current[key]:
            return False
    return True


def trace_fieldline_trajectory(
    field,
    R0: float,
    Z0: float,
    phi_start: float,
    phi_end: float,
    DPhi: float,
    *,
    dphi_out: float | None = None,
    chunk_phi_span: float | None = None,
    storage: str = "auto",
    checkpoint_dir: str | os.PathLike[str] | None = None,
    resume: bool = True,
    memory_limit_bytes: int = 512 * 1024 * 1024,
    extend_phi: bool = True,
    stop_after_chunks: int | None = None,
) -> DenseFieldLineTrajectory:
    """Trace a field line with dense interpolation and restart checkpoints.

    The returned :class:`DenseFieldLineTrajectory` provides ``sol(phi)`` like
    scipy's dense-output objects.  If ``checkpoint_dir`` is supplied, trajectory
    arrays are stored as disk-backed ``.npy`` memmaps and a JSON checkpoint is
    updated after every complete chunk.  Re-running with identical parameters and
    ``resume=True`` continues from the last complete chunk.
    """

    if storage not in {"auto", "memory", "disk"}:
        raise ValueError("storage must be 'auto', 'memory', or 'disk'")
    phi_start = float(phi_start)
    phi_end = float(phi_end)
    DPhi = abs(float(DPhi))
    if DPhi <= 0.0 or not np.isfinite(DPhi):
        raise ValueError("DPhi must be finite and positive")
    span = phi_end - phi_start
    if dphi_out is None:
        dphi_out = DPhi
    dphi_out = float(np.copysign(abs(float(dphi_out)), span if span != 0.0 else 1.0))
    if abs(dphi_out) <= 0.0 or not np.isfinite(dphi_out):
        raise ValueError("dphi_out must be finite and non-zero")

    n_total = _expected_n_samples(phi_start, phi_end, dphi_out)
    bytes_needed = n_total * (3 * np.dtype(np.float64).itemsize + np.dtype(np.int8).itemsize)

    if chunk_phi_span is None:
        chunk_phi_span = min(abs(span) if span else abs(dphi_out), 8.0 * 2.0 * np.pi)
    chunk_intervals = max(1, int(math.floor(abs(float(chunk_phi_span)) / abs(dphi_out))))

    ckpt_dir = Path(checkpoint_dir) if checkpoint_dir is not None else None
    if storage == "disk" or (storage == "auto" and (ckpt_dir is not None or bytes_needed > memory_limit_bytes)):
        if ckpt_dir is None:
            ckpt_dir = Path(tempfile.mkdtemp(prefix="pyna_fieldline_"))
        ckpt_dir.mkdir(parents=True, exist_ok=True)
        storage_mode = "disk"
    else:
        storage_mode = "memory"

    params = {
        "R0": float(R0),
        "Z0": float(Z0),
        "phi_start": phi_start,
        "phi_end": phi_end,
        "DPhi": DPhi,
        "dphi_out": dphi_out,
        "chunk_intervals": int(chunk_intervals),
    }
    checkpoint_path = ckpt_dir / _CHECKPOINT_NAME if ckpt_dir is not None else None

    checkpoint = _load_checkpoint(checkpoint_path) if checkpoint_path is not None and resume else None
    if checkpoint is not None:
        if checkpoint.get("schema_version") != _SCHEMA_VERSION or not _params_match(checkpoint.get("params", {}), params):
            raise ValueError(f"checkpoint parameters do not match current trace: {checkpoint_path}")
        R_all = _open_array(ckpt_dir, "R")
        Z_all = _open_array(ckpt_dir, "Z")
        phi_all = _open_array(ckpt_dir, "phi")
        alive_all = _open_array(ckpt_dir, "alive")
        n_written = int(checkpoint["n_written"])
        next_index = int(checkpoint["next_index"])
        R_cur = float(checkpoint["last_R"])
        Z_cur = float(checkpoint["last_Z"])
        phi_cur = float(checkpoint["last_phi"])
    else:
        disk_dir = ckpt_dir if storage_mode == "disk" else None
        try:
            R_all = _allocate_array(disk_dir, "R", (n_total,), np.float64, fill_value=np.nan)
            Z_all = _allocate_array(disk_dir, "Z", (n_total,), np.float64, fill_value=np.nan)
            phi_all = _allocate_array(disk_dir, "phi", (n_total,), np.float64, fill_value=np.nan)
            alive_all = _allocate_array(disk_dir, "alive", (n_total,), np.int8, fill_value=0)
        except MemoryError:
            if storage == "memory":
                raise
            if ckpt_dir is None:
                ckpt_dir = Path(tempfile.mkdtemp(prefix="pyna_fieldline_"))
                checkpoint_path = ckpt_dir / _CHECKPOINT_NAME
            ckpt_dir.mkdir(parents=True, exist_ok=True)
            R_all = _allocate_array(ckpt_dir, "R", (n_total,), np.float64, fill_value=np.nan)
            Z_all = _allocate_array(ckpt_dir, "Z", (n_total,), np.float64, fill_value=np.nan)
            phi_all = _allocate_array(ckpt_dir, "phi", (n_total,), np.float64, fill_value=np.nan)
            alive_all = _allocate_array(ckpt_dir, "alive", (n_total,), np.int8, fill_value=0)
            storage_mode = "disk"

        R_all[0] = float(R0)
        Z_all[0] = float(Z0)
        phi_all[0] = phi_start
        alive_all[0] = 1
        n_written = 1
        next_index = 0
        R_cur = float(R0)
        Z_cur = float(Z0)
        phi_cur = phi_start

    arrays = field_arrays_from_field(field, extend_phi=extend_phi)
    chunks_done_this_call = 0
    completed = n_written >= n_total

    while not completed:
        start_idx = next_index
        end_idx = min(n_total - 1, start_idx + chunk_intervals)
        phi_chunk_end = phi_start + dphi_out * end_idx
        if end_idx == n_total - 1:
            phi_chunk_end = phi_end

        R_t, Z_t, phi_t, _DP_t, alive_t = trace_orbit_along_phi(
            R_cur,
            Z_cur,
            phi_cur,
            phi_chunk_end,
            DPhi,
            arrays.R_grid,
            arrays.Z_grid,
            arrays.Phi_grid,
            arrays.BR_flat,
            arrays.BZ_flat,
            arrays.BPhi_flat,
            dphi_out=abs(dphi_out),
        )

        valid = np.asarray(alive_t, dtype=bool) & np.isfinite(R_t) & np.isfinite(Z_t) & np.isfinite(phi_t)
        if not np.any(valid):
            break
        last_valid_local = int(np.nonzero(valid)[0][-1])
        if last_valid_local == 0:
            break
        tol_phi = max(1.0e-12, 1.0e-12 * max(1.0, abs(phi_chunk_end)))
        terminated_early = abs(float(phi_t[last_valid_local]) - float(phi_chunk_end)) > tol_phi

        src = slice(1, last_valid_local + 1)
        dst_start = start_idx + 1
        dst_end = dst_start + (last_valid_local)
        R_all[dst_start:dst_end] = R_t[src]
        Z_all[dst_start:dst_end] = Z_t[src]
        phi_all[dst_start:dst_end] = phi_t[src]
        alive_all[dst_start:dst_end] = np.asarray(alive_t[src], dtype=np.int8)

        n_written = max(n_written, dst_end)
        next_index = dst_end - 1
        R_cur = float(R_all[next_index])
        Z_cur = float(Z_all[next_index])
        phi_cur = float(phi_all[next_index])
        chunks_done_this_call += 1

        completed = next_index >= n_total - 1
        status = "complete" if completed else "running"
        if checkpoint_path is not None:
            payload = _checkpoint_payload(
                checkpoint_dir=ckpt_dir,
                status=status,
                params=params,
                n_total=n_total,
                n_written=n_written,
                next_index=next_index,
                last_R=R_cur,
                last_Z=Z_cur,
                last_phi=phi_cur,
                storage=storage_mode,
            )
            _atomic_write_json(checkpoint_path, payload)
        for arr in (R_all, Z_all, phi_all, alive_all):
            if hasattr(arr, "flush"):
                arr.flush()

        if stop_after_chunks is not None and chunks_done_this_call >= int(stop_after_chunks):
            completed = False
            break
        if terminated_early:
            completed = False
            break

    final_status = "complete" if completed else "incomplete"
    if checkpoint_path is not None:
        payload = _checkpoint_payload(
            checkpoint_dir=ckpt_dir,
            status=final_status,
            params=params,
            n_total=n_total,
            n_written=n_written,
            next_index=next_index,
            last_R=R_cur,
            last_Z=Z_cur,
            last_phi=phi_cur,
            storage=storage_mode,
        )
        _atomic_write_json(checkpoint_path, payload)

    usable = slice(0, n_written)
    metadata = {
        "status": final_status,
        "R0": float(R0),
        "Z0": float(Z0),
        "phi_start": phi_start,
        "phi_end": phi_end,
        "DPhi": DPhi,
        "dphi_out": dphi_out,
        "storage": storage_mode,
        "checkpoint": str(checkpoint_path) if checkpoint_path is not None else None,
    }
    return DenseFieldLineTrajectory(
        phi=phi_all[usable],
        R=R_all[usable],
        Z=Z_all[usable],
        alive=alive_all[usable],
        metadata=metadata,
        storage_dir=str(ckpt_dir) if ckpt_dir is not None else None,
    )


def trace_fieldline_trajectory_bidirectional(
    field,
    R0: float,
    Z0: float,
    phi_start: float,
    phi_span: float,
    DPhi: float,
    **kwargs,
) -> dict[str, DenseFieldLineTrajectory]:
    """Trace one seed forward and backward, returning two dense trajectories."""

    span = abs(float(phi_span))
    kwargs_fwd = dict(kwargs)
    kwargs_bwd = dict(kwargs)
    ckpt = kwargs.get("checkpoint_dir")
    if ckpt is not None:
        kwargs_fwd["checkpoint_dir"] = Path(ckpt) / "forward"
        kwargs_bwd["checkpoint_dir"] = Path(ckpt) / "backward"
    return {
        "forward": trace_fieldline_trajectory(
            field, R0, Z0, phi_start, float(phi_start) + span, DPhi, **kwargs_fwd
        ),
        "backward": trace_fieldline_trajectory(
            field, R0, Z0, phi_start, float(phi_start) - span, DPhi, **kwargs_bwd
        ),
    }


def _trace_fieldline_trajectory_task(field, R0, Z0, phi_start, phi_end, DPhi, kwargs):
    return trace_fieldline_trajectory(field, R0, Z0, phi_start, phi_end, DPhi, **kwargs)


def _optional_prefect():
    try:
        from pyna.workflow.prefect import optional_prefect
    except ModuleNotFoundError as exc:
        if exc.name in {"pyna.workflow", "pyna.workflow.prefect"}:
            raise RuntimeError(
                "Prefect support requires the optional workflow module; "
                "install pyna with the Prefect extra."
            ) from exc
        raise
    return optional_prefect()


def _build_prefect_fieldline_flow(flow, task):
    trajectory_task = task(_trace_fieldline_trajectory_task)

    @flow(name="pyna-fieldline-trajectory")
    def prefect_fieldline_trajectory_flow(field, R0, Z0, phi_start, phi_end, DPhi, **kwargs):
        return trajectory_task(field, R0, Z0, phi_start, phi_end, DPhi, kwargs)

    return prefect_fieldline_trajectory_flow


def fieldline_trajectory_flow(field, R0, Z0, phi_start, phi_end, DPhi, **kwargs):
    """Prefect flow for restartable field-line trajectory tracing.

    Prefect handles orchestration and retries.  Bitwise resume consistency is
    still provided by :func:`trace_fieldline_trajectory`, which writes complete
    chunk checkpoints and resumes from the last committed chunk.
    """

    flow, task = _optional_prefect()
    prefect_flow = _build_prefect_fieldline_flow(flow, task)
    return prefect_flow(field, R0, Z0, phi_start, phi_end, DPhi, **kwargs)


def build_prefect_fieldline_flow():
    """Return the canonical Prefect flow for field-line trajectory tracing."""

    flow, task = _optional_prefect()
    return _build_prefect_fieldline_flow(flow, task)


